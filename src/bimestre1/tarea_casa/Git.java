package tarea_casa;

public class Git {
    public static void main(String[] args) {
        System.out.println("COMANDOS GIT PARA COLOCAR ARCHIVOS EN GITHUB:\n");
        System.out.println("git init: sirve para iniciar un repositorio git en la carpeta seleccionada");
        System.out.println("git add.: sirve para añadir archivos que aún no han sido subidos a git");
        System.out.println("git commit -m '...': sirve para poner los archivos y las líneas de comando en las carpetas de github");
        System.out.println("git branch -M main: sirve para colocar los archivos en la rama pricipal de github");
        System.out.println("git remote add origin https://github.com/santyago0/claseprogramacion.git: sirve para poner el origen de los archivos en la página de github del usuario");
        System.out.println("git push -u origin main: sirve para subir los archivos y que se visualicen en el link del usuario de github");
        System.out.println("git status: sirve para ver el progreso de las actualizaciones de los archivos y para saber si falta añadir alguno");
        System.out.println("git user.email/name: sirve para configurar el correo electrónico y el nombre del usuario de github");
    }
}
